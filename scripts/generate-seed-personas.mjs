import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const csvPath = path.join(
  __dirname,
  '..',
  'BBDD Grupos Conexion - Si quieren grupo V3 (1) (1).csv',
)
const outPath = path.join(__dirname, '..', 'convex', 'seedPersonasData.ts')

function parseCsvLine(line) {
  const fields = []
  let current = ''
  let inQuotes = false
  for (let i = 0; i < line.length; i++) {
    const ch = line[i]
    if (ch === '"') {
      inQuotes = !inQuotes
      continue
    }
    if (ch === ',' && !inQuotes) {
      fields.push(current)
      current = ''
      continue
    }
    current += ch
  }
  fields.push(current)
  return fields
}

function isBlank(value) {
  const t = (value ?? '').trim()
  return !t || t === '-'
}

function normalizeDocumento(value, fallback) {
  const t = (value ?? '').trim()
  if (isBlank(t)) return fallback
  return t.toLowerCase()
}

function cleanName(value) {
  return (value ?? '').trim()
}

function cleanContact(value) {
  const t = (value ?? '').trim()
  if (isBlank(t)) return undefined
  return t
}

const raw = fs.readFileSync(csvPath, 'latin1')
const lines = raw.split(/\r?\n/).filter(Boolean)
const dataLines = lines.slice(1)

/** @type {Array<{ row: number, side: 'hombre'|'mujer', nombreCompleto: string, documento: string, contacto?: string, parejaNombre?: string, pairKey: string }>} */
const entries = []

for (let i = 0; i < dataLines.length; i++) {
  const rowNum = i + 2
  const cols = parseCsvLine(dataLines[i])
  const hombreNombre = cleanName(cols[2])
  const mujerNombre = cleanName(cols[7])
  const hombreDoc = normalizeDocumento(cols[1], `sin-cedula-fila-${rowNum}-hombre`)
  const mujerDoc = normalizeDocumento(cols[6], `sin-cedula-fila-${rowNum}-mujer`)
  const pairKey = `fila-${rowNum}`

  if (!isBlank(hombreNombre)) {
    entries.push({
      row: rowNum,
      side: 'hombre',
      nombreCompleto: hombreNombre,
      documento: hombreDoc,
      contacto: cleanContact(cols[3]),
      parejaNombre: isBlank(mujerNombre) ? undefined : mujerNombre,
      pairKey,
    })
  }

  if (!isBlank(mujerNombre)) {
    entries.push({
      row: rowNum,
      side: 'mujer',
      nombreCompleto: mujerNombre,
      documento: mujerDoc,
      contacto: cleanContact(cols[8]),
      parejaNombre: isBlank(hombreNombre) ? undefined : hombreNombre,
      pairKey,
    })
  }
}

const seenDocs = new Set()
for (const entry of entries) {
  if (seenDocs.has(entry.documento)) {
    throw new Error(`Duplicate documento ${entry.documento} at row ${entry.row}`)
  }
  seenDocs.add(entry.documento)
}

const serialized = entries.map((e) => {
  const parts = [
    `row: ${e.row}`,
    `side: '${e.side}'`,
    `nombreCompleto: ${JSON.stringify(e.nombreCompleto)}`,
    `documento: ${JSON.stringify(e.documento)}`,
  ]
  if (e.contacto) parts.push(`contacto: ${JSON.stringify(e.contacto)}`)
  if (e.parejaNombre) parts.push(`parejaNombre: ${JSON.stringify(e.parejaNombre)}`)
  parts.push(`pairKey: ${JSON.stringify(e.pairKey)}`)
  return `  { ${parts.join(', ')} },`
})

const output = `// Generated by scripts/generate-seed-personas.mjs — do not edit by hand
export type SeedPersona = {
  row: number
  side: 'hombre' | 'mujer'
  nombreCompleto: string
  documento: string
  contacto?: string
  parejaNombre?: string
  pairKey: string
}

export const SEED_PERSONAS: SeedPersona[] = [
${serialized.join('\n')}
]
`

fs.writeFileSync(outPath, output)
console.log(`Wrote ${entries.length} personas to ${outPath}`)
